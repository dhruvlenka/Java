public class DoWhileLoop3 {
     public static void main(String[] args) {
        do {
          System.out.println("Hello");
        }
       while(10>20);
          // This condition is false, This will print Hello,Hi!
         System.out.println("Hi");
          /*
           If condition will be true, then Hello will print infinite times.
           Curly brackets doesn't matter for 1 output.
          */
     } 
}
